/**
 * Generates an az CLI bash script that assigns the Reader role to the
 * App Registration service principal on each subscription.
 */
export function generateAzureSetupScript(
  tenantName: string,
  clientId: string,
  subscriptionIds: string[]
): string {
  const escapedClientId = shellEscape(clientId);
  const roleAssignments = subscriptionIds
    .map(
      (id) =>
        `az role assignment create \\\n  --assignee "\${APP_ID}" \\\n  --role "Reader" \\\n  --scope "/subscriptions/${shellEscape(id)}"`
    )
    .join("\n\n");

  return `#!/bin/bash
# Datadog Azure Integration Setup (generated by Datadog Connect)
# Tenant: ${tenantName}

set -e

echo "=== Azure Datadog統合セットアップ ==="

# 1. App Registration 作成 (既存の場合はスキップ)
echo "1. App Registrationを確認中..."
APP_ID=${escapedClientId}

# 2. 各サブスクリプションにReaderロールを付与
echo "2. Readerロールを付与中..."
${roleAssignments}

echo ""
echo "=== セットアップ完了 ==="
echo "Datadogでの統合確認をしてください。"
`;
}

