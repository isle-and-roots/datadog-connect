/**
 * Generates the Agent installation bash script for Linux/macOS.
 * For Windows, use generateWindowsInstallScript() instead.
 */
export function generateLinuxInstallScript(
  os: string,
  tags: string[],
  flags: OnPremFeatureFlags
): string {
  const tagStr = tags.length > 0 ? tags.join(",") : "";
  const tagLine = tagStr ? `DD_HOST_TAGS=${shellEscape(tagStr)}` : "";

  return `#!/bin/bash
# Datadog Agent Install - ${os}
# Generated by Datadog Connect

set -e

DD_API_KEY="<YOUR_DD_API_KEY>"
${tagLine}

echo "=== Datadog Agent インストール ==="

DD_AGENT_MAJOR_VERSION=7 DD_API_KEY="\${DD_API_KEY}" \\
  ${tagLine ? `DD_HOST_TAGS="\${DD_HOST_TAGS}" \\` : ""}
  bash -c "$(curl -L ${AGENT_INSTALL_SCRIPT_URL})"

${
  flags.enableProcess
    ? `# プロセス監視有効化
echo "process_config:" >> /etc/datadog-agent/datadog.yaml
echo "  process_collection:" >> /etc/datadog-agent/datadog.yaml
echo "    enabled: true" >> /etc/datadog-agent/datadog.yaml`
    : ""
}

${
  flags.enableNetwork
    ? `# ネットワーク監視有効化
echo "network_config:" >> /etc/datadog-agent/system-probe.yaml
echo "  enabled: true" >> /etc/datadog-agent/system-probe.yaml`
    : ""
}

# Agent再起動
systemctl restart datadog-agent

echo "=== インストール完了 ==="
datadog-agent status | head -20
`;
}

/**
 * Escapes special characters for safe interpolation inside PowerShell double-quoted strings.
 * Escapes backtick, double-quote, and dollar sign.
 */
function escapePowerShell(s: string): string {
  return s.replace(/[`"$]/g, '`$&');
}

/**
 * Generates the Agent installation PowerShell script for Windows.
 */
export function generateWindowsInstallScript(tags: string[]): string {
  const tagList = tags.map(escapePowerShell).join(",");
  return `# Datadog Agent Install - Windows
# Run in PowerShell as Administrator

$env:DD_API_KEY = "<YOUR_DD_API_KEY>"
${tagList ? `$env:DD_HOST_TAGS = "${tagList}"` : ""}

. { Set-ExecutionPolicy Bypass -Scope Process; [Net.ServicePointManager]::SecurityProtocol = [Net.SecurityProtocolType]::Tls12; iex ((New-Object System.Net.WebClient).DownloadString('${AGENT_INSTALL_SCRIPT_WINDOWS_URL}')) }
`;
}

